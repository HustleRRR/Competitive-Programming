// Find Set LSB = (x&(-x)), isPowerOfTwo = (x & (x-1))
 
const int mod = 998244353;
unsigned long long power(unsigned long long x,
                                  int y, int p)
{
    unsigned long long res = 1;
 
    x = x % p;
 
    while (y > 0)
    {
        if (y & 1)
            res = (res * x) % p;
        y = y >> 1;
        x = (x * x) % p;
    }
    return res;
}
 
unsigned long long modInverse(unsigned long long n, 
                                            int p)
{
    return power(n, p - 2, p);
}
 
unsigned long long nCr(unsigned long long n,
                                 int r, int p)
{
    if (n < r)
        return 0;
    if (r == 0)
        return 1;
    unsigned long long fac[n + 1];
    fac[0] = 1;
    for (int i = 1; i <= n; i++)
        fac[i] = (fac[i - 1] * i) % p;
 
    return (fac[n] * modInverse(fac[r], p) % p
            * modInverse(fac[n - r], p) % p)
           % p;
}

void solve(){
    int n,k;
    cin >> n >> k;
    string s;
    cin >> s;
    vi ones;
    for(int i=0; i<n; i++){
        if(s[i] == '1')ones.pb(i);
    }
    if(k == 0 || k > ones.size()){
        cout << 1;
        return;
    }
    int ans = 0;
    int m = (int)ones.size();
    for(int i=0; i+k-1 < m; i++){
        int l = ones[i], r = ones[i+k-1];
        int len = (r-l+1);
        l--, r++;
        while(l >=0 && s[l] == '0'){
            len++;
            l--;
        }
        while(r<=n-1 && s[r] == '0'){
            len++;
            r++;
        }
        debug(len, k);
        ans += nCr(len, k, mod);
        ans %= mod;
    }
    ans = (ans - m + mod)%mod;
    cout << ans;
}
